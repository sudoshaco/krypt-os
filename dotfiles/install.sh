#!/bin/bash
# dotfiles/install.sh вҖ” Krypt OS Dotfiles Installer
#
# Erstellt Symlinks von diesem Repo nach ~/.config/ (und andere Ziele).
# Ist idempotent вҖ” mehrfaches AusfГјhren macht nichts kaputt.
#
# Verwendung:
#   cd ~/krypt-os && ./dotfiles/install.sh
#   cd ~/krypt-os && ./dotfiles/install.sh --dry-run   # zeigt was passieren wГјrde
#   cd ~/krypt-os && ./dotfiles/install.sh --force      # Гјberschreibt existierende Dateien

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"

DRY_RUN=0
FORCE=0

while [[ $# -gt 0 ]]; do
    case "$1" in
        --dry-run) DRY_RUN=1; shift ;;
        --force)   FORCE=1;   shift ;;
        *) echo "Unbekannt: $1"; exit 1 ;;
    esac
done

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# Helpers
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
GREEN='\033[0;32m'; YELLOW='\033[1;33m'; RED='\033[0;31m'
CYAN='\033[0;36m'; DIM='\033[2m'; RESET='\033[0m'

ok()   { echo -e "${GREEN}  вң“${RESET} $*"; }
skip() { echo -e "${DIM}  -${RESET} $*"; }
warn() { echo -e "${YELLOW}  !${RESET} $*"; }
info() { echo -e "${CYAN}  вҶ’${RESET} $*"; }
err()  { echo -e "${RED}  вң—${RESET} $*" >&2; }

do_link() {
    local src="$1"
    local dst="$2"

    if [[ $DRY_RUN -eq 1 ]]; then
        info "[dry-run] $dst вҶ’ $src"
        return
    fi

    # Ziel-Verzeichnis anlegen
    mkdir -p "$(dirname "$dst")"

    if [[ -L "$dst" ]]; then
        # Bereits ein Symlink вҖ” prГјfen ob er auf das Richtige zeigt
        if [[ "$(readlink -f "$dst")" == "$(readlink -f "$src")" ]]; then
            skip "$dst (bereits verlinkt)"
            return
        else
            warn "$dst zeigt auf falsches Ziel вҖ” ersetze"
        fi
    elif [[ -e "$dst" ]]; then
        if [[ $FORCE -eq 1 ]]; then
            warn "$dst existiert вҖ” --force: wird ersetzt"
            local backup="${dst}.krypt-backup-$(date +%Y%m%d%H%M%S)"
            mv "$dst" "$backup"
            warn "  Backup: $backup"
        else
            err "$dst existiert bereits (kein Symlink). Nutze --force um zu ersetzen."
            return 1
        fi
    fi

    ln -sf "$src" "$dst"
    ok "$dst"
}

